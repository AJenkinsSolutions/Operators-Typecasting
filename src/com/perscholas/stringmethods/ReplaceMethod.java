package com.perscholas.stringmethods;

public class ReplaceMethod {

	public static void main(String[] args) {
		/**
		 * Replaces a specified characters in a string with a new character and returns it
		 */
		String s1 = "abc 456";
		System.out.println(s1.replace('a', '1'));
		System.out.println(s1.replace('b', '2'));
		System.out.println(s1.replace('c', '3' ));

		


	}

}
